class Person {
    
    var firstname: String
    var lastname: String
    var street: String
    var place: String
    var zip: String
    
    init(firstname: String, lastname: String, street: String, place: String, zip: String) {
        self.firstname = firstname
        self.lastname = lastname
        self.street = street
        self.place = place
        self.zip = zip
    }
}
